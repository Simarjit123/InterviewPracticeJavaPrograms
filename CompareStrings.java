package practice;

public class CompareStrings {

	public static void main(String[] args) {

		String name1 = "Simarjit";
		String name2 = "simarjit";
		System.out.println("compare with equals: " + name1.equals(name2));

		System.out.println("compare with equal ignore case :" + name1.equalsIgnoreCase(name2));

	}

}
